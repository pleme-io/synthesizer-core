// ─── Root trait ─────────────────────────────────────────────────────

/// An artifact: any provably-rendered deliverable produced by the typescape.
///
/// Implementations MUST guarantee:
/// - [`Artifact::name`] is stable across calls.
/// - [`Artifact::kind`] is stable across calls.
/// - [`Artifact::files`] is pure: same internal state → same bytes.
///
/// The default [`Artifact::canonical_bytes`] produces a deterministic
/// encoding suitable for content-addressing.
pub trait Artifact {
    /// Canonical name of this artifact.
    fn name(&self) -> &str;

    /// Taxonomic tag.
    fn kind(&self) -> ArtifactKind;

    /// Decompose into file atoms. Every artifact ultimately materializes
    /// as files on disk or blobs in a registry.
    fn files(&self) -> Vec<FileArtifact>;

    /// Deterministic byte encoding of the file set. Callers hash this
    /// (BLAKE3, SHA-256) to content-address the artifact.
    ///
    /// This default impl sorts files by path and length-prefixes each
    /// path and blob. Byte-equal file sets → byte-equal encodings.
    fn canonical_bytes(&self) -> Vec<u8> {
        canonical_encode(&self.files())
    }
}

/// Deterministic encoding of a file set.
///
/// Algorithm: sort by path, then for each file emit
/// `len(path_bytes) ++ path_bytes ++ len(content) ++ content`, all
/// length prefixes as little-endian u64. Byte-equal file sets
/// (regardless of input ordering) → byte-equal output.
#[must_use]
pub fn canonical_encode(files: &[FileArtifact]) -> Vec<u8> {
    let mut sorted: Vec<&FileArtifact> = files.iter().collect();
    sorted.sort_by(|a, b| a.path.cmp(&b.path));

    let mut out = Vec::new();
    for f in sorted {
        let path_bytes = f.path.as_os_str().as_encoded_bytes();
        out.extend_from_slice(&(path_bytes.len() as u64).to_le_bytes());
        out.extend_from_slice(path_bytes);
        out.extend_from_slice(&(f.bytes.len() as u64).to_le_bytes());
        out.extend_from_slice(&f.bytes);
    }
    out
}

// ─── FileArtifact — the atom ─────────────────────────────────────────

/// One file: a path and its bytes. The atomic artifact.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct FileArtifact {
    /// Relative or absolute path.
    pub path: PathBuf,
    /// File contents.
    pub bytes: Vec<u8>,
}

impl FileArtifact {
    /// Construct from a path and arbitrary bytes.
    #[must_use]
    pub fn new(path: impl Into<PathBuf>, bytes: impl Into<Vec<u8>>) -> Self {
        Self {
            path: path.into(),
            bytes: bytes.into(),
        }
    }

    /// Construct from a path and UTF-8 text (the common case).
    #[must_use]
    pub fn text(path: impl Into<PathBuf>, text: impl Into<String>) -> Self {
        let s: String = text.into();
        Self::new(path, s.into_bytes())
    }

    /// View bytes as UTF-8. `None` if invalid.
    #[must_use]
    pub fn as_str(&self) -> Option<&str> {
        std::str::from_utf8(&self.bytes).ok()
    }

    /// Byte length.
    #[must_use]
    pub fn len(&self) -> usize {
        self.bytes.len()
    }

    /// True when zero bytes.
    #[must_use]
    pub fn is_empty(&self) -> bool {
        self.bytes.is_empty()
    }

    /// Borrow the target path.
    #[must_use]
    pub fn path(&self) -> &Path {
        &self.path
    }
}

impl Artifact for FileArtifact {
    fn name(&self) -> &str {
        self.path
            .file_name()
            .and_then(|s| s.to_str())
            .unwrap_or("")
    }

    fn kind(&self) -> ArtifactKind {
        ArtifactKind::File
    }

    fn files(&self) -> Vec<FileArtifact> {
        vec![self.clone()]
    }
}

