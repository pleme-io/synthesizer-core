//! Artifact hierarchy — the provable outputs of the typescape.
//!
//! > "The big deal here is we render artifacts provably and reliably
//! > through typescape and AST."
//!
//! An `Artifact` is ANY deliverable produced by rendering typed AST: a file,
//! a workspace directory, a git repo, a Nix flake, a Ruby gem, an npm package,
//! a Helm chart, an OCI container image, an AMI, a Terraform module, …
//!
//! ## Taxonomy alignment
//!
//! [`ArtifactKind`] MIRRORS substrate's Nix-level `artifactKind` enum from
//! `substrate/lib/types/foundation.nix` (binary, library, service,
//! docker-image, wasm-component, wasi-service, helm-chart, npm-package, gem,
//! scaffold, overlay) and adds the higher-level pleme-io concepts the
//! typescape reasons over (workspace, repo, flake, constellation, fleet,
//! ami, store-path, terraform-module, terraform-provider).
//!
//! Adding a new `ArtifactKind` variant is a coordinated change with
//! substrate — do not silently diverge.
//!
//! ## The chain
//!
//! ```text
//! Rust types  →  typed AST (SynthesizerNode)  →  bytes  →  Artifact  →  disk / registry / cluster
//! ```
//!
//! Every artifact's `canonical_bytes()` is a deterministic encoding of its
//! file set. Hash that with BLAKE3/SHA-256 and you have a content-address.
//! Two runs of the same typescape state produce byte-identical artifacts;
//! any divergence is a proof failure, not an ambient variation.

use std::path::{Path, PathBuf};

// ─── Taxonomy ────────────────────────────────────────────────────────

/// Tag for the shape of an artifact.
///
/// The first 11 variants mirror substrate's `artifactKind` (see
/// `substrate/lib/types/foundation.nix`). The remaining variants extend
/// the taxonomy with pleme-io typescape concepts.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ArtifactKind {
    /// A single file (path + bytes). The atomic artifact.
    File,

    // ── Substrate parity ──
    /// Executable binary.
    Binary,
    /// Reusable code library (crate, lib, .a, .so).
    Library,
    /// Long-running service.
    Service,
    /// OCI container image.
    DockerImage,
    /// WebAssembly component.
    WasmComponent,
    /// WASI-compatible service.
    WasiService,
    /// Packaged Helm chart.
    HelmChart,
    /// npm package.
    NpmPackage,
    /// Ruby gem.
    Gem,
    /// Scaffold / template (skeleton files).
    Scaffold,
    /// Nix overlay.
    Overlay,

    // ── pleme-io typescape extensions ──
    /// A directory pack that represents one convergence target (e.g.,
    /// `workspaces/platform-iam/`).
    Workspace,
    /// A git repository.
    Repo,
    /// A Nix flake (repo with `flake.nix` at the root).
    Flake,
    /// A multi-workspace composition (e.g., `pangea-architectures/`).
    Constellation,
    /// A deployment fleet pack (e.g., `fleet.yaml` + workspace mapping).
    Fleet,
    /// An Amazon Machine Image (or similar baked cloud image).
    Ami,
    /// A Nix store path (already built, already cached).
    StorePath,
    /// A Terraform module.
    TerraformModule,
    /// A Terraform provider (generated by terraform-forge).
    TerraformProvider,
}

impl ArtifactKind {
    /// Stable kebab-case identifier. The first 11 variants EXACTLY match
    /// substrate's `artifactKind` enum strings — this is a grep-able
    /// attestation of taxonomy parity.
    #[must_use]
    pub const fn as_str(&self) -> &'static str {
        match self {
            Self::File => "file",
            Self::Binary => "binary",
            Self::Library => "library",
            Self::Service => "service",
            Self::DockerImage => "docker-image",
            Self::WasmComponent => "wasm-component",
            Self::WasiService => "wasi-service",
            Self::HelmChart => "helm-chart",
            Self::NpmPackage => "npm-package",
            Self::Gem => "gem",
            Self::Scaffold => "scaffold",
            Self::Overlay => "overlay",
            Self::Workspace => "workspace",
            Self::Repo => "repo",
            Self::Flake => "flake",
            Self::Constellation => "constellation",
            Self::Fleet => "fleet",
            Self::Ami => "ami",
            Self::StorePath => "store-path",
            Self::TerraformModule => "terraform-module",
            Self::TerraformProvider => "terraform-provider",
        }
    }

    /// True iff this kind is part of substrate's canonical `artifactKind`
    /// enum. Used by invariant tests to detect drift.
    #[must_use]
    pub const fn is_substrate_canonical(&self) -> bool {
        matches!(
            self,
            Self::Binary
                | Self::Library
                | Self::Service
                | Self::DockerImage
                | Self::WasmComponent
                | Self::WasiService
                | Self::HelmChart
                | Self::NpmPackage
                | Self::Gem
                | Self::Scaffold
                | Self::Overlay
        )
    }
}

