// ─── Laws ───────────────────────────────────────────────────────────

/// Property-checkable laws for any [`Artifact`] implementation.
pub mod laws {
    use super::{canonical_encode, Artifact, FileArtifact};

    /// `canonical_bytes` is deterministic: repeated calls return the same bytes.
    #[must_use]
    pub fn canonical_bytes_deterministic<A: Artifact>(a: &A) -> bool {
        a.canonical_bytes() == a.canonical_bytes()
    }

    /// `files` is pure: repeated calls return the same file set.
    #[must_use]
    pub fn files_is_pure<A: Artifact>(a: &A) -> bool {
        a.files() == a.files()
    }

    /// `canonical_encode` is order-independent across any permutation of
    /// the input slice. Different input orderings → same output.
    #[must_use]
    pub fn canonical_encode_order_independent(a: &[FileArtifact], b: &[FileArtifact]) -> bool {
        // Precondition (caller ensures): `a` and `b` are permutations of
        // the same set. Result: canonical_encode treats them equally.
        canonical_encode(a) == canonical_encode(b)
    }

    /// `name` is stable across calls.
    #[must_use]
    pub fn name_is_stable<A: Artifact>(a: &A) -> bool {
        a.name() == a.name()
    }

    /// `kind` is stable across calls.
    #[must_use]
    pub fn kind_is_stable<A: Artifact>(a: &A) -> bool {
        a.kind() == a.kind()
    }
}

