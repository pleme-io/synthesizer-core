    proptest::proptest! {
        #[test]
        fn prop_canonical_bytes_deterministic_any_workspace(
            name in "[a-z]{1,10}",
            files in proptest::collection::vec(("[a-z]{1,5}", "[a-z]{0,20}"), 0..10)
        ) {
            let ws = Workspace::new(name).with_files(
                files.into_iter().map(|(p, b)| FileArtifact::text(p, b)).collect()
            );
            proptest::prop_assert!(laws::canonical_bytes_deterministic(&ws));
        }

        #[test]
        fn prop_file_set_reorder_yields_same_encoding(
            files in proptest::collection::vec(("[a-z]{1,8}", "[a-z]{0,10}"), 1..8)
        ) {
            // Deduplicate paths so reordering is meaningful.
            let mut seen = std::collections::HashSet::new();
            let unique: Vec<FileArtifact> = files.into_iter()
                .filter(|(p, _)| seen.insert(p.clone()))
                .map(|(p, b)| FileArtifact::text(p, b))
                .collect();

            let mut reversed = unique.clone();
            reversed.reverse();

            proptest::prop_assert_eq!(
                canonical_encode(&unique),
                canonical_encode(&reversed)
            );
        }
    }
